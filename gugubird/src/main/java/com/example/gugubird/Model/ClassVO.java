package com.example.gugubird.Model;

import java.util.Date;

public class ClassVO {
   String className;
   String seminarTime;
   String seminatPosition;
   String filePath;
   String fileName;

    public String getClassName() {
        return className;
    }

    public void setClassName(String className) {
        this.className = className;
    }

    public String getSeminarTime() {
        return seminarTime;
    }

    public void setSeminarTime(String seminarTime) {
        this.seminarTime = seminarTime;
    }

    public String getSeminatPosition() {
        return seminatPosition;
    }

    public void setSeminatPosition(String seminatPosition) {
        this.seminatPosition = seminatPosition;
    }

    public String getFilePath() {
        return filePath;
    }

    public void setFilePath(String filePath) {
        this.filePath = filePath;
    }

    public String getFileName() {
        return fileName;
    }

    public void setFileName(String fileName) {
        this.fileName = fileName;
    }
}
